package cobasendiri;
public class OperatorAritmatika {
    public static void main(String[] args) {
        
        System.out.println("Hello, Ahsan");
        System.out.println("Program Operator Aritmatika\nDalam Java");

        int a,b,c;

        a = 10;
        b = 12;
        c = a + b;

        //  Kalau println cuma bisa cetak String jadinya gini
        System.out.println(a+ " + "+b+" = "+c);

        //  Kalau printf bisa print integer
        System.out.printf("%d + %d = %d\n", a, b,c);

        System.out.printf("Hello Ahsan");

        
    }
}
