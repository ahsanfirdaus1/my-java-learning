package cobasendiri;
public class IfElse1 {
    
    public static void main(String[] args) {
        
        System.out.println("Hello, Ahsan");
        System.out.println("Program If Else Java 1");

        int ahsan = 1;
        String hasil;

        if (ahsan == 1){
            hasil = "Ya, Benar";
        } else if (ahsan >= 1) {
            hasil = "Bisa jadi kembarannya kali yaa";
        } else {
            hasil = "Yang bener??";
        }

        System.out.println("Hasil : "+hasil);
        
    }
}
