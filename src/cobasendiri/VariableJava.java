package cobasendiri;
public class VariableJava {
    public static void main(String[] args) {
        
        System.out.println("Hello, Ahsan");
        System.out.println("Program Variable Pada Java");

        int a = 9;
        System.out.println("Angka: "+ a);

        a = 92;
        System.out.println("Angka: "+ a);

        int b;
        b = 100;
        System.out.println("Angka: "+b);

        int c = a + b;
        System.out.println("Penjumlahan dari "+a+" + "+b+" adalah "+ c);

    }
}
